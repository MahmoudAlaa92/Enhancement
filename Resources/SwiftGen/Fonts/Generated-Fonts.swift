import UIKit

extension UIFont {
    static func nunitoBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Nunito-Bold", size: size) ?? .systemFont(ofSize: size)
    }
    
    static func semiboldItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "SF-Pro-Text-SemiboldItalic", size: size) ?? .systemFont(ofSize: size)
    }
}
